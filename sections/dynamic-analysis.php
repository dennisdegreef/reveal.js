        <h1>Dynamic analysis</h1>
    </section>

    <section>
        <h2>Definition</h2>

        <p>
            The analysis of computer software that is performed by executing programs on a real or virtual processor.<br/>
            <br/>
            For dynamic program analysis to be effective,
            <br/>the target program must be executed with sufficient test inputs to produce interesting behavior.<br/>
            <br/>
            Use of software testing measures such as code coverage helps ensure that an adequate slice of the program's set of possible behaviors has been observed.

        </p>
    </section>

    <section>
        <h2>Callstack</h2>
        <p>
            A callstack is the order in which statements are exectuted.<br />
            A common callstack, is an Exception trace. This trace shows all the statements executed before an exception is thrown.
        </p>
    </section>

    <section>
        <h2>Callgraph</h2>
        <p>
            A callgraph is a visual representation of a callstack.<br />
            In large applications this graph can give you better insight on how an application is wired.
        </p>
    </section>

    <section>
        <h2>Profiling data</h2>

        <p>
            Usually, the data gathered with a profiler can be represented in stacks or graphs.<br />
            They can include information regarding memory- and cpu-usage.
        </p>
    </section>
